# Python "if-statements"
# _________________________________________________________________________________________

age = None
# _________________________________________________________________________________________

# if (steht IMMER am Anfang)

if age < 18:
    print("Too young.")
# _________________________________________________________________________________________

# else (steht IMMER am Ende)

if age < 18:
    print("Too young.")
else:
    print("Welcome!")
# _________________________________________________________________________________________

# elif (steht IMMER nach "if" und vor "else")

if age < 18:
    print("Too young.")

elif age == 18:
    print("Welcome.")
# _________________________________________________________________________________________
