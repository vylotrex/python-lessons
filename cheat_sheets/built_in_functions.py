# Python Built-In Functions
# _________________________________________________________________________________________

# print()
# Gibt einen Datentyp in der Konsole aus

print("Hallo Welt")
print(3.141592654)
# _________________________________________________________________________________________

# input()
# Fragt einen Benutzer nach einer Eingabe und gibt diese als String zurück

name = input("Wie heißt du?")
address = input("Wo wohnst du?")
# _________________________________________________________________________________________

# len()
# Gibt die Länge (Anzahl der Elemente) eines Objekts zurück

len("Hallo") # 5
len("Hallo Welt") # 10
# _________________________________________________________________________________________

# type()
# Gibt den Typ eines Objekts zurück

type(5.83) # <class 'float'>
type("Hallo Welt") # <class 'str'>
# _________________________________________________________________________________________

# str(), int(), float(), bool(), list()
# Wandelt einen Wert in den Entsprechenden Datentyp um

int("123") # 123
list("123") # ['1','2','3']
# _________________________________________________________________________________________
